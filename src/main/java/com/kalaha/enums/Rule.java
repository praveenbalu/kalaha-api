package com.kalaha.enums;

/**
 * Enumeration to hold the list of rules
 * @author Praveen
 */
public enum Rule {

	/** The capture stones. */
	CAPTURE_STONES,

	/** The capture own stones. */
	CAPTURE_OWN_STONES,

	/** The capture opponent stones on empty. */
	CAPTURE_OPPONENT_STONES_ONEMPTY,

	/** The player turn changed. */
	PLAYER_TURN_CHANGED,

	/** The game over. */
	GAME_OVER
}
